#include<bits/stdc++.h>
using namespace std;
#define ll long long 
int main()
{
    ll t;
    cin>>t;
    for(ll i = 1 ; i<=t ; i++)
    {
        ll n;
        cin>>n;
        for(ll i = 1 ; i<=n ; i++)
        {
            for(ll j = 1 ; j<=n ; j++)
            {
                cout<<1<<" ";
            }
            cout<<endl;
        }

    }
    return 0;
}